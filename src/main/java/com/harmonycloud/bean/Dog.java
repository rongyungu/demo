package com.harmonycloud.bean;


public class Dog {
    private String name;
    private String age;

    public Dog(){}
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAge() {
        return age;
    }

    public void setAge(String age) {
        this.age = age;
    }
}
